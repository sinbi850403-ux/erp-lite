export async function loginWithEmail(email, password) {
  if (!isSupabaseConfigured) {
    showToast('Login configuration is missing. Please check Supabase settings.', 'warning');
    return null;
  }

  if (isLoggingIn) {
    showToast('Login is already in progress. Please wait a moment.', 'info');
    return null;
  }
  isLoggingIn = true;

  const loginBtn = document.getElementById('gate-email-login');
  const originalText = loginBtn?.textContent || 'Login with Email';

  if (loginBtn) {
    loginBtn.disabled = true;
    loginBtn.textContent = 'Logging in...';
    loginBtn.style.opacity = '0.7';
  }

  document.getElementById('login-error-msg')?.remove();

  try {
    try {
      await withTimeout(supabase.auth.signOut({ scope: 'local' }), 3000, 'pre-login-signout');
    } catch {
      // Continue login flow even if local sign-out fails.
    }
    purgeLegacyAuthStorage({ includeSupabaseSession: true });

    async function attemptLogin(attempt = 1) {
      const timeout = attempt === 1 ? 15000 : 20000;
      try {
        const { data, error } = await withTimeout(
          supabase.auth.signInWithPassword({ email, password }),
          timeout,
          'login',
        );
        if (error) throw error;
        return data.user;
      } catch (err) {
        const message = String(err?.message || '').toLowerCase();
        const isNetwork = message.includes('fetch') || message.includes('network') || message.includes('timeout') || message.includes('abort');
        if (isNetwork && attempt === 1) {
          await new Promise((resolve) => setTimeout(resolve, 1500));
          purgeLegacyAuthStorage({ includeSupabaseSession: true });
          return directPasswordLogin(email, password);
        }
        throw err;
      }
    }

    const user = await attemptLogin(1).catch(async (err) => {
      const message = String(err?.message || '').toLowerCase();
      const isNetwork = message.includes('fetch') || message.includes('network') || message.includes('timeout') || message.includes('abort');
      if (isNetwork) {
        await new Promise((resolve) => setTimeout(resolve, 2000));
        return attemptLogin(2);
      }
      throw err;
    });

    showToast((user?.user_metadata?.full_name || 'User') + ' logged in.', 'success');
    return toCompatUser(user);
  } catch (error) {
    const lowerMessage = String(error?.message || '').toLowerCase();

    let errorMsg = '';
    let showResetAction = false;

    const isConnectivity =
      lowerMessage.includes('fetch') ||
      lowerMessage.includes('network') ||
      lowerMessage.includes('timeout') ||
      lowerMessage.includes('abort');

    if (
      lowerMessage.includes('invalid login') ||
      lowerMessage.includes('invalid credentials') ||
      lowerMessage.includes('email or password') ||
      lowerMessage.includes('invalid email or password')
    ) {
      errorMsg = 'Email or password is incorrect.';
      showResetAction = true;
      showToast('Please check your login credentials.', 'error', 3000);
    } else if (lowerMessage.includes('email not confirmed')) {
      errorMsg = 'Email verification is not complete.';
      showToast(errorMsg, 'warning', 5000);
    } else if (isConnectivity) {
      const debug = getSupabaseDebugInfo();
      if (typeof navigator !== 'undefined' && navigator.onLine === false) {
        errorMsg = 'You are offline. Please check your network and retry.';
      } else {
        errorMsg = 'Auth server is delayed. Please retry in a moment.';
      }
      console.error('[Auth] Network/login error (after retries)', {
        message: error?.message,
        name: error?.name,
        online: navigator?.onLine,
        supabase: debug,
      });
      showToast(errorMsg, 'error', 8000);
    } else {
      errorMsg = 'Login failed: ' + (error?.message || 'Unknown error');
      showToast(errorMsg, 'error');
    }

    renderInlineLoginError(loginBtn, email, errorMsg, showResetAction);
    return null;
  } finally {
    isLoggingIn = false;

    if (loginBtn) {
      loginBtn.disabled = false;
      loginBtn.textContent = originalText;
      loginBtn.style.opacity = '1';
    }
  }
}
